public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello and welcome!");

        for (int i = 1; i <= 10; i++) {
            System.out.println(i + "\n\n");
        }

        // Iterating from 1 to 5
        for (int i = 1; i <= 5; i++) {
            System.out.println("i = " + i);
        }
    }
}