public class Variables {
    public static void main(String[] args) {
        /*
            * Variables are used to store data. In Java, we have to declare the type of the
            * variable before using it. The syntax is as follows:
            *
            * <type> <variableName> = <value>;
            *
            * Here, <type> is the type of the variable, <variableName> is the name of the
            * variable, and <value> is the value that we want to store in the variable.
            *
            * For example, in the following line of code, we are declaring a variable of
            * type int with the name myNumber and storing the value 10 in it.
         */
        int myNumber = 10;
        System.out.println(myNumber);
    }
}
