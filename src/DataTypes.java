public class DataTypes {
    public static void main(String[] args) {
        /*
         * In Java, we have different data types to store different types of data. The
         * most commonly used data types are int, float, double, char, and boolean.
         *
         * int: Used to store integer values. For example, 10, 20, 30, etc.
         *
         * float: Used to store floating-point values. For example, 10.5, 20.5, 30.5,
         * etc.
         *
         * double: Used to store double-precision floating-point values. For example,
         * 10.5, 20.5, 30.5, etc.
         *
         * char: Used to store single characters. For example, 'a', 'b', 'c', etc.
         *
         * boolean: Used to store true or false values.
         *
         * String: Used to store a sequence of characters. For example, "Hello, World!",
         *
         * The syntax to declare a variable of a specific data type is as follows:
         *
         * <type> <variableName> = <value>;
         *
         * Here, <type> is the data type of the variable, <variableName> is the name of
         * the variable, and <value> is the value that we want to store in the variable.
         *
         * For example, in the following line of code, we are declaring a variable of
         * type int with the name myNumber and storing the value 10 in it.
         */
        int myNumber = 10;
        System.out.println(myNumber);

        // Declaring a variable of type float
        float myFloat = 10.5f;
        System.out.println(myFloat);

        // Declaring a variable of type double
        double myDouble = 10.5;
        System.out.println(myDouble);

        // Declaring a variable of type char
        char myChar = 'a';
        System.out.println(myChar);

        // Declaring a variable of type String
        String myString = "Hello, World!";
        System.out.println(myString);

        // Declaring a variable of type boolean
        boolean myBoolean = true;
        System.out.println(myBoolean);
    }
}
